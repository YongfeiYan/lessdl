def drop(input, p, training=True, inplace=True):
    if p == 0:
        return input
    if not training:
        return _back_dropout(input, p, training, inplace)
    assert len(input.shape) == 3
    if len(input.shape) == 3:
        zero_mask = input == 0
        input[:, :, ::2] = p
        input.masked_fill_(zero_mask, 0)
    return input
# F.dropout(input, self.p, self.training, self.inplace)
_back_dropout = F.dropout


def drop_noaction(input, p, training=True, inplace=True):
    return input


def replace_dropout(nodrop=False):
    if nodrop:
        F.dropout = drop_noaction
    else:
        F.dropout = drop


def unreplace_dropout():
    F.dropout = _back_dropout


def build_trainer(argline, rm_exp=True):
    argline = argline.split()
    args = parse_args(arglist=argline)

    train_data = TranslationDataset.build(args, 'train')
    print('train_data', train_data)
    n_lines = len(list(iter(train_data)))

    arch = get_arch_arch(args.arch)
    arch(args)
    print(args)
    model = Transformer.build(args, train_data)

    if os.path.exists(args.exp_dir) and rm_exp:
        shutil.rmtree(args.exp_dir)
    trainer = BasicTrainer(args, model, train_dataset=train_data, valid_dataset=train_data)

    return trainer, n_lines, train_data


